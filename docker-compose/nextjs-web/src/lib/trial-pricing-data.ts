// ── Excel descending approximate match (MATCH(...,-1)) ───────────────────────
/** Smallest key ≥ value; falls back to the smallest key when value exceeds none. */
export function matchDesc(value: number, keysDesc: number[]): number {
  let chosen = keysDesc[keysDesc.length - 1];
  for (const k of keysDesc) {
    if (k >= value) chosen = k;
  }
  return chosen;
}

/** 2D matrix lookup by (diameter, length) using descending approx match. */
export interface Matrix {
  diameters: number[]; // descending
  lengths: number[]; // descending
  /** values[dia][len] — null = 不可 ('-' in Excel). */
  values: Record<number, Record<number, number | null>>;
}

export function lookupMatrix(
  m: Matrix,
  diameter: number,
  length: number,
): number | null {
  const d = matchDesc(diameter, m.diameters);
  const l = matchDesc(length, m.lengths);
  return m.values[d]?.[l] ?? null;
}

